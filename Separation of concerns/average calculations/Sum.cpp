#include "Sum.h"

//overloaded functions
double calculateSum(double num1, double num2)
{
    return (num1+num2);
}

double calculateSum(double num1, double num2, double num3)
{
    return (num1 + num2 + num3);
}

double calculateSum(double num1, double num2, double num3, double num4)
{
    return(num1 + num2 + num3 + num4);
}

