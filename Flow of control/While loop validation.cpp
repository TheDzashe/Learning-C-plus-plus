#include <iostream>
using namespace std;

int main()
{
    int i = 0;
    while (i<5)
    {
        cout << "The value of i is: " << i << endl;
        i++;
    }
    cout << "The value of i is now: " << i << endl;
    cout << "___________" << endl;
    while (i > 0)
    {
        cout << "The value of i is: " << i << endl;
        i--;
    }

   
}
